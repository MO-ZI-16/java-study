package Test;

public class Test3 {
    public static void main(String[] args){
        for(int i = 1;i<=5;i++){
            System.out.println(i);
        }
        for(int i = 5;i>=1;i--){
            System.out.println(i);
        }
        for(int i = 1;i<=5;i++){
            System.out.println("第"+i+"次断线重连");
        }
    }
}
