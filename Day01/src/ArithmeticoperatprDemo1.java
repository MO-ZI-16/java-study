public class ArithmeticoperatprDemo1 {
    public static  void  main(String[] args){
        //+
        System.out.println(3+2);
        //-
        System.out.println(3-2);
        //*
        System.out.println(3*2);

        //在计算的时候有小数参与
        //在代码中,如果有小数参与计算,结论有可能不精确的
        System.out.println(1.1+1.01);
    }
}
